let usersByDay = [812, 3672, 1200, 342, 54321];

// чтобы узнать длину массива, используется функция .length
console.log(usersByDay.length); // 5

// можно вывести последний элемент массива через эту функцию
console.log(usersByDay[usersByDay.length - 1]);  // 54321
console.log(usersByDay[usersByDay.length - 3]);  // 1200



